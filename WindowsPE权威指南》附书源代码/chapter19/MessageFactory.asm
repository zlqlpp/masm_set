;-------------------------------------------
; 窗口枚举函数
;-------------------------------------------
_EnumProc proc uses ebx hTopWinWnd:DWORD,value:DWORD
      call @F   ; 免去重定位
@@:
      pop ebx
      sub ebx,offset @B
      .if hTopWinWnd!=NULL
        push sizeof szClassNameBuf
        mov eax,offset szClassNameBuf
        add eax,ebx
        push eax
        push hTopWinWnd
        call [ebx+_GetClassNameA]
        push sizeof szWndTextBuf
        mov eax,offset szWndTextBuf
        add eax,ebx
        push eax
        push hTopWinWnd
        call [ebx+_GetClassNameA]
        pushad
        mov esi,offset szName
        add esi,ebx

        mov edi,offset szWndTextBuf
        add edi,ebx
        mov ecx,10
        repe cmpsb
        jnz  @2
        mov eax,hTopWinWnd
        mov dword ptr [ebx+hWin],eax
@2:
        popad
      .endif
      mov eax,hTopWinWnd ;当窗口句柄为空时结束
      ret 
_EnumProc endp


